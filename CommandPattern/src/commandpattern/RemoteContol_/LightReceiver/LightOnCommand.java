package commandpattern.RemoteContol_.LightReceiver;

import CommandPattern.RemoteContol_.Command;

/**
 *
 * @author A_Murad
 */
public class LightOnCommand implements Command {

    private Light light;

    public LightOnCommand(Light light) {
        this.light = light;
    }

    @Override
    public void execute() {
        light.turnOn();
    }

}
